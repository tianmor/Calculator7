function subtract(a,b){
    return a-b
}

function multiply(a, b){
    return a*b
}

function divide(a,b){
    return a/b
}

function square(a){
    return a*a
}

let a = 5
let b = 3
console.log("SUBTRACT: ", subtract(a, b))
