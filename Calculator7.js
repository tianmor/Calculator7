function add(a,b){
    return a+b
}

function subtract(a,b){
    return a-b
}

function multiply(a, b){
    return a*b
}

function divide(a,b){
    return a/b
}

let a = 5
let b = 3
console.log("ADDITION: ", add(a, b))
console.log("SUBTRACT: ", subtract(a, b))
console.log("MULTIPLICATION: ", multiply(a,b))
